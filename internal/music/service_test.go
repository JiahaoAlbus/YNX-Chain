func TestAllocationSettlementAndAuthorization(t *testing.T) {
	s := testService(t)
	creator := testAccount(t, 5)
	other := testAccount(t, 6)
	listener := testAccount(t, 7)
	track := publishTrack(t, s, creator, false)
	s.UpsertProfile(listener, Profile{DisplayName: "Listener"})
	_, usage, e := s.SavePosition(listener, track.ID, "paid-usage", 1200, true)
	if e != nil {
		t.Fatal(e)
	}
	if _, e = s.Allocate(other, "receipt-1", 1000, []string{usage.ID}); e != ErrUnauthorized {
		t.Fatalf("other creator allocated usage: %v", e)
	}
	a, e := s.Allocate(creator, "external-revenue-record-1", 1000, []string{usage.ID})
	if e != nil {
		t.Fatal(e)
	}
	intent, e := s.Settlement(creator, a.ID, creator)
	if e != nil {
		t.Fatal(e)
	}
	if intent.Status != "requires_wallet_review" || intent.AmountMicros != 1000 {
		t.Fatalf("settlement truth boundary lost: %#v", intent)
	}
	if _, e = s.Settlement(creator, a.ID, creator); e != ErrConflict {
		t.Fatalf("duplicate settlement = %v", e)
	}
}

