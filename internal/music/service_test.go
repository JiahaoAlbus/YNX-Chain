func testAccount(t *testing.T, fill byte) string {
	t.Helper()
	raw := "0x" + strings.Repeat(string("0123456789abcdef"[fill%16]), 40)
	out, err := accountaddress.Encode(raw)
	if err != nil {
		t.Fatal(err)
	}
	return out
}
